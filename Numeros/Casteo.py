print("Castear flotantes")

x = float(1) #x sera 1.0
y = float(2.8) #y sera 2.8
z = float("3") #z sera un 3.0
w = float("4.2") #w sera 4.2

print(x)
print(y)
print(z)
print(w)

print("Castear cadenas")

x = str("s1") #x sera 's1'
